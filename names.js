function nameRng(rng){return rng?rng():Math.random()}
function namePick(arr,rng){return arr[Math.floor(nameRng(rng)*arr.length)]}
function composeGivenName(gender,rng){
  const r=nameRng(rng);
  const a=namePick(GIVEN_A_100,rng);
  if(r<0.26)return a;
  if(r<0.74)return a+namePick(GIVEN_B_100,rng);
  return a+a;
}
function randomChineseFullName(gender,rng){
  return namePick(SURNAME_100,rng)+composeGivenName(gender,rng);
}
function randomChineseGivenName(gender,rng){
  return composeGivenName(gender,rng);
}
function pickPartnerDisplayName(gender){
  return randomChineseGivenName(gender);
}
function pickBffName(playerGender){
  const g=playerGender==='female'?'female':'male';
  return randomChineseGivenName(g);
}
function pickStrangerDisplayName(gender){
  if(Math.random()<0.12){
    const c=namePick(GIVEN_A_100);
    return Math.random()<0.5?('小'+c):('阿'+c);
  }
  return randomChineseFullName(gender);
}
